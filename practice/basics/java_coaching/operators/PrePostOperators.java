public class PrePostOperators {
    public static void main(String[] args) {
        int t = 5;
        int x = 4;
        int y = 1;
        int z = 2;
        while (t-- > 0) {
            System.out.println(x);
            System.out.println(y);
            System.out.println(z);
        }
    }
}
