vector<int> solve(int N, vector<int> A, int X)
{

    vector<int> sol;
    int count = 0;

    for (auto i : A)
    {
        if (i == X)
        {
            count++;
        }
        else
        {
            sol.push_back(i);
        }
    }

    for (size_t i = 0; i < count; i++)
    {
        sol.insert(sol.begin(), 1);
    }
    // Write your code here
    return sol;
}

